def human_delay(max_seconds=300):
    """Wait for a random amount of time to mimic human behavior."""
    delay = random.uniform(5, max_seconds)
    logger.info(f"Waiting for {delay:.2f} seconds to simulate human behavior...")
    time.sleep(delay)
